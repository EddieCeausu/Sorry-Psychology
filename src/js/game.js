function questionGetter() {
  var q,
    a;
  var options = [];
  var str = "";
  var rand = Math.floor(Math.random() * Math.floor(questions.length - 1));
  if (questions[rand].includes("::")) {
    a = questions[rand];
    q = questions[rand - 1];
    questions.splice(rand - 1, 2);
    console.log(q, '\n', a);
  } else {
    q = questions[rand];
    a = questions[rand + 1]
    questions.splice(rand, 2);
    console.log(q, '\n', a);
  }
  answered.unshift(q, a);

  for (var i = q.length - 1; i >= 0; i--) {
    if (q[i] != "\n") {
      str = q[i] + str;
    } else {
      options.unshift(str);
      str = "";
    }
  }
  var i = 0;
  for (i = 0; i < q.length; i++) {
    if (q[i] == "\n") {
      break;
    }
  }
  return {
    question: q.slice(0, i - 1).toString(),
    option: options,
    answer: a.slice(2, 3).toString()
  }
}
